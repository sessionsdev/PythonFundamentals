'''

If a runner runs 10 miles in 30 minutes and 30 seconds,
What is his/her average speed in kilometers per hour? (Tip: 1 mile = 1.6 km)

'''
time = 1830  # running time in seconds
miles = 10
kilometers = miles * 1.6

k_per_h = (kilometers/time) * 3600

print(k_per_h)

# 31 kilomters per hour.  That's top 10 fastest people in the world level.
